import UIKit

extension UIColor {
    class var paleGrey: UIColor {
        return UIColor(red: 236.0 / 255.0, green: 239.0 / 255.0, blue: 241.0 / 255.0, alpha: 1.0)
    }
    
    class var bluishGrey: UIColor {
        return UIColor(red: 120.0 / 255.0, green: 144.0 / 255.0, blue: 156.0 / 255.0, alpha: 1.0)
    }
}
